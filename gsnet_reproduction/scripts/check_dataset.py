#!/usr/bin/env python3
"""Check whether a GraspNet-style dataset is ready for GSNet reproduction."""

from __future__ import annotations

import argparse
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_DATASET_ROOT = REPO_ROOT / "data" / "graspnet"


def count_dirs(path: Path, pattern: str) -> int:
    if not path.exists():
        return 0
    return sum(1 for item in path.glob(pattern) if item.is_dir())


def count_files(path: Path, pattern: str) -> int:
    if not path.exists():
        return 0
    return sum(1 for item in path.glob(pattern) if item.is_file())


def status_line(ok: bool, label: str, detail: str = "") -> None:
    prefix = "[OK]" if ok else "[MISS]"
    suffix = f" - {detail}" if detail else ""
    print(f"{prefix} {label}{suffix}")


def check_file(path: Path) -> bool:
    ok = path.exists()
    status_line(ok, str(path))
    return ok


def check_files(paths: list[Path]) -> bool:
    results = [check_file(path) for path in paths]
    return all(results)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--dataset-root",
        type=Path,
        default=DEFAULT_DATASET_ROOT,
        help=f"Dataset root. Default: {DEFAULT_DATASET_ROOT}",
    )
    parser.add_argument("--camera", default="kinect", choices=["kinect", "realsense"])
    parser.add_argument("--scene", default="scene_0000")
    parser.add_argument("--frame", default="0000")
    args = parser.parse_args()

    root = args.dataset_root.expanduser().resolve()
    print(f"Dataset root: {root}")
    print(f"Camera: {args.camera}")
    print(f"Sample: {args.scene}/{args.frame}")

    if not root.exists():
        print("[FAIL] dataset root does not exist")
        return 1

    scenes = root / "scenes"
    train_1 = root / "train_1"
    collision_label = root / "collision_label"
    grasp_label = root / "grasp_label"
    grasp_label_simplified = root / "grasp_label_simplified"
    graspness = root / "graspness"

    print("\nTop-level directories")
    status_line(scenes.exists(), "scenes/", "GSNet expected scene directory")
    status_line(train_1.exists(), "train_1/", "local raw scene directory")
    status_line(collision_label.exists(), "collision_label/")
    status_line(grasp_label.exists(), "grasp_label/", "original labels")
    status_line(grasp_label_simplified.exists(), "grasp_label_simplified/", "generated by simplify_dataset.py")
    status_line(graspness.exists(), "graspness/", "generated by generate_graspness.py")

    scene_count = count_dirs(scenes, "scene_*")
    train_count = count_dirs(train_1, "scene_*")
    collision_count = count_dirs(collision_label, "scene_*")
    grasp_label_count = count_files(grasp_label, "*_labels.npz")
    simplified_count = count_files(grasp_label_simplified, "*_labels.npz")

    print("\nCounts")
    print(f"[INFO] scenes scene count: {scene_count}")
    print(f"[INFO] train_1 scene count: {train_count}")
    print(f"[INFO] collision_label scene count: {collision_count}")
    print(f"[INFO] grasp_label files: {grasp_label_count}")
    print(f"[INFO] grasp_label_simplified files: {simplified_count}")

    print("\nLocal raw sample")
    local_sample_files = [
        train_1 / args.scene / args.camera / "depth" / f"{args.frame}.png",
        train_1 / args.scene / args.camera / "label" / f"{args.frame}.png",
        train_1 / args.scene / args.camera / "meta" / f"{args.frame}.mat",
        train_1 / args.scene / args.camera / "camera_poses.npy",
        train_1 / args.scene / args.camera / "cam0_wrt_table.npy",
        collision_label / args.scene / "collision_labels.npz",
    ]
    local_ok = check_files(local_sample_files)

    print("\nGSNet expected sample")
    gsnet_sample_files = [
        scenes / args.scene / args.camera / "depth" / f"{args.frame}.png",
        scenes / args.scene / args.camera / "label" / f"{args.frame}.png",
        scenes / args.scene / args.camera / "meta" / f"{args.frame}.mat",
        scenes / args.scene / args.camera / "camera_poses.npy",
        scenes / args.scene / args.camera / "cam0_wrt_table.npy",
        graspness / args.scene / args.camera / f"{args.frame}.npy",
        grasp_label_simplified / "000_labels.npz",
    ]
    gsnet_ok = check_files(gsnet_sample_files)

    print("\nInterpretation")
    if not scenes.exists() and train_1.exists():
        print("[ACTION] GSNet expects root/scenes. For smoke tests, create:")
        print(f"         ln -s {train_1} {scenes}")
    if train_count and train_count < 100:
        print("[WARN] local train_1 has fewer than 100 train scenes; default GSNet training expects scene_0000..scene_0099")
    if not grasp_label_simplified.exists():
        print("[ACTION] run external/graspness_unofficial/dataset/simplify_dataset.py")
    if not graspness.exists():
        print("[ACTION] run external/graspness_unofficial/dataset/generate_graspness.py")

    if gsnet_ok:
        print("[PASS] dataset has the expected GSNet sample structure")
        return 0
    if local_ok:
        print("[PARTIAL] raw dataset sample exists, but generated GSNet folders/layout are not ready")
        return 2

    print("[FAIL] raw dataset sample is incomplete")
    return 1


if __name__ == "__main__":
    raise SystemExit(main())
